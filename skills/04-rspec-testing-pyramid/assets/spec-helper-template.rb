# spec/spec_helper.rb — drop-in template.
# SimpleCov MUST be the first require — even before Rails — to instrument all files.

require "simplecov"

SimpleCov.start "rails" do
  add_filter "/spec/"
  add_filter "/config/"
  add_filter "/vendor/"
  add_filter "/db/"
  add_filter "/bin/"

  add_group "Models",      "app/models"
  add_group "Controllers", "app/controllers"
  add_group "Services",    "app/services"
  add_group "Jobs",        "app/jobs"
  add_group "Mailers",     "app/mailers"
  add_group "Helpers",     "app/helpers"

  minimum_coverage 80
  minimum_coverage_by_file 70

  enable_coverage :branch  # Ruby 2.5+ branch coverage
end

# This file was generated by the `rails generate rspec:install` command. Conventionally, all
# specs live under a `spec` directory, which RSpec adds to the `$LOAD_PATH`. The generated
# `.rspec` file contains `--require spec_helper` which will cause this file to always be
# loaded, without a need to explicitly require it in any files.
RSpec.configure do |config|
  config.expect_with :rspec do |expectations|
    expectations.include_chain_clauses_in_custom_matcher_descriptions = true
  end

  config.mock_with :rspec do |mocks|
    mocks.verify_partial_doubles = true
    mocks.verify_doubled_constant_names = true
  end

  config.shared_context_metadata_behavior = :apply_to_host_groups

  # Fail fast on the first failure in CI
  config.fail_fast = ENV["FAIL_FAST"] == "true"

  # Use the documentation formatter for detailed output in CI
  config.formatter = ENV["CI"] ? :documentation : :progress

  config.filter_run_when_matching :focus
  config.example_status_persistence_file_path = "spec/examples.txt"
  config.disable_monkey_patching!

  config.order = :random
  Kernel.srand config.seed
end
